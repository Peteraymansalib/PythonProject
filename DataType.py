print(type(50))  # int
print(type(-100))  # int

print(type(0.5))  # float
print(type(-1.8484))  # float

print(type("Hello World"))  # str ==> String

print(type([1, 2, 3, 4, 5]))  # list

print(type((1, 2, 3, 4, 5)))  # tuple

print(type({"one": 1, "two": 2, "three": 3}))  # Dict ==> Dictionary

print(type(2 == 2))  # bool ==> Boolean (true)
print(type(2 == 4))  # bool ==> Boolean (False)
